    private void BtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBuscarActionPerformed
        if (TxtMatricula.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Informe a Matricula");
            TxtMatricula.requestFocus();
        } else {
            modelAlunos.setMatricula(TxtMatricula.getText());

            if (controllerAlunos.getValidarAlunoController(modelAlunos)){
                modelAlunos = controllerAlunos.retornarAlunoMatriculaController(modelAlunos.getMatricula());
                AlunosSingleton.getInstance().setNome(modelAlunos.getNome());
                AlunosSingleton.getInstance().setPerfil(modelAlunos.getPerfil());
                AlunosSingleton.getInstance().setPk_id_Aluno(modelAlunos.getId());
                this.dispose();
                new view.form_Refeicao().setVisible(true);                
            }else{
                JOptionPane.showMessageDialog(this,"Matricula NÃ£o Encontrada", "Aviso", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_BtnBuscarActionPerformed

